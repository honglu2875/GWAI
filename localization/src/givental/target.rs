//! A calibration adapter and its generic semisimple provider.
//!
//! A [`SemisimpleTarget`] combines one canonical [`GwTheory`] with the reconstruction
//! data that are specific to a semisimple calibration: the classical
//! eigenvalue seeds of divisor multiplication at the torus fixed points and
//! the quantum/classical divisor multiplication matrices.  Geometry such as
//! dimension, the effective cone, and first-Chern pairings is never restated
//! by this adapter.  [`TargetProvider`] turns any such calibration into a
//! [`SemisimpleCohftProvider`] by running the quantum-ring recipe: Newton
//! root series -> Lagrange canonical frame -> Bernoulli asymptotics from the
//! seed differences -> flatness recursion, and the descendant `S`-matrix from
//! the quantum differential equation. [`GwTarget`] preserves the original
//! projective-insertion contract and adapts into this generic boundary.
//!
//! Current scope (deliberate, documented restrictions):
//!
//! - one Novikov variable (curve-class rank one); rank-two targets such as
//!   `P^n x P^m` need the multi-parameter series layer first;
//! - the state space is generated by a single divisor class with the flat
//!   basis `1, D, ..., D^rank-1`, so idempotents come from Lagrange
//!   interpolation and the metric is the residue pairing of the presentation.
//!
//! [`ProjectiveTarget`] is the reference implementation; the crate's original
//! `ProjectiveSpaceProvider` remains the production path and the two are held
//! equal by tests.

